#include "../includes/libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (str[i] != '\0' && str)
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	return (n);
}
