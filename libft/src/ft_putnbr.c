#include "../includes/libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
		ft_putnbr(n);
	}
	else if (n == 0)
	{
		ft_putstr("0");
	}
	else
	{
		n /= 10;
		ft_putnbr(n);
	}
	ft_putchar((n % 10) + '0');
}
